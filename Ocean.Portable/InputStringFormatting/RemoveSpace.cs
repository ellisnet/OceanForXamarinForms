namespace Ocean.Portable.InputStringFormatting {
    /// <summary>
    /// Options for removing multiple spaces from the text string.
    /// </summary>
    public enum RemoveSpace {

        /// <summary>
        /// Remove multiple spaces from the text string.
        /// </summary>
        MultipleSpaces,

        /// <summary>
        /// Remove all spaces from the text string.
        /// </summary>
        AllSpaces,

        /// <summary>
        /// Do not remove any spaces from the text string.
        /// </summary>
        None

    }
}
